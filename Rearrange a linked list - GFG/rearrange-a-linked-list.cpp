class Solution
{
    public:
    void rearrangeEvenOdd(Node *head)
    {
        if(head==NULL or head->next==NULL) return;
        
        Node *odd_head=head, *odd_curr=odd_head, *even_head=head->next,*even_curr=even_head;
        
        while(odd_curr and even_curr and odd_curr->next and even_curr->next)
        {
            odd_curr->next=odd_curr->next->next;
            even_curr->next=even_curr->next->next;
            
            odd_curr=odd_curr->next;
            even_curr=even_curr->next;
        }
        
    
        odd_curr->next=even_head;
        head=odd_head;
        return;
        
    }
};


